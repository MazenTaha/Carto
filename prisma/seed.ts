async function main() {
    console.log('Start seeding global food products...')

    let count = 0
    let skipped = 0
    const now = new Date()

    const adminPasswordHash = await bcrypt.hash(ADMIN_PASSWORD, 12)
    await prisma.user.upsert({
        where: { email: ADMIN_EMAIL },
        update: {
            password: adminPasswordHash,
            name: 'Admin',
        },
        create: {
            email: ADMIN_EMAIL,
            password: adminPasswordHash,
            name: 'Admin',
        },
    })

    for (const product of products) {
        const normalizedPrice = Number.isFinite(product.price) && product.price > 0
            ? product.price
            : DEFAULT_BASE_PRICE_EGP
        // Use findFirst with mode: 'insensitive' to correctly skip duplicates
        const existing = await prisma.product.findFirst({
            where: { name: { equals: product.name, mode: 'insensitive' } },
            select: {
                id: true,
                price: true,
            },
        })

        if (!existing) {
            await prisma.product.create({
                data: {
                    name: product.name,
                    category: product.category,
                    emoji: product.emoji || null,
                    price: normalizedPrice,
                    popularity: product.popularity
                }
            })
            count++
        } else {
            if (!(existing.price > 0)) {
                await prisma.product.update({
                    where: { id: existing.id },
                    data: {
                        price: normalizedPrice,
                    },
                })
            }
            skipped++
        }
    }

    const store = await prisma.store.upsert({
        where: { id: DEMO_STORE_ID },
        update: {},
        create: {
            id: DEMO_STORE_ID,
            name: 'Carto Demo Store',
            location: 'Development seed',
        },
    })

    await prisma.$transaction(async (tx) => {
        for (const preset of DEMO_CART_PRESETS) {
            const existingCart = await tx.cart.findFirst({
                where: {
                    OR: [
                        { cartCode: preset.cartCode },
                        ...((preset.legacyCartCodes ?? []).map((legacyCode) => ({ cartCode: legacyCode }))),
                    ],
                },
                select: {
                    id: true,
                },
            })

            const hasActiveSession = existingCart
                ? Boolean(await tx.cartSession.findFirst({
                    where: {
                        cartId: existingCart.id,
                        status: { in: [SessionStatus.ACTIVE, SessionStatus.DISCONNECTED] },
                        endedAt: null,
                    },
                    select: { id: true },
                }))
                : false
            const targetCartStatus = hasActiveSession ? 'IN_USE' : 'AVAILABLE'

            if (existingCart) {
                await tx.cart.update({
                    where: { id: existingCart.id },
                    data: {
                        cartCode: preset.cartCode,
                        bluetoothName: getDemoCartBluetoothName(preset.cartCode),
                        pairingCode: null,
                        pairingExpiresAt: null,
                        qrSessionId: null,
                        deviceSecret: preset.deviceSecret,
                        status: targetCartStatus,
                        storeId: store.id,
                        lastSeen: now,
                    },
                })
            } else {
                await tx.cart.create({
                    data: {
                        cartCode: preset.cartCode,
                        bluetoothName: getDemoCartBluetoothName(preset.cartCode),
                        pairingCode: null,
                        pairingExpiresAt: null,
                        qrSessionId: null,
                        deviceSecret: preset.deviceSecret,
                        storeId: store.id,
                        status: targetCartStatus,
                        lastSeen: now,
                    },
                })
            }
        }
    })

    const totalCount = await prisma.product.count()
    console.log(`Seeding finished. Added ${count} new products, skipped ${skipped} duplicates.`)
    console.log(`Total unique products in database: ${totalCount}`)
    console.log(`Admin test account ready: ${ADMIN_EMAIL}`)
    console.log(`Demo cart ready: ${DEMO_CART_CODE}`)
    console.log(`Simulator default cart ready: ${DEFAULT_SIMULATOR_CART_CODE}`)
}

main()
    .then(async () => {
        await prisma.$disconnect()
    })
    .catch(async (e) => {
        console.error(e)
        await prisma.$disconnect()
        process.exit(1)
    })
